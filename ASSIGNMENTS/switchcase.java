package nikitaApplication;

public class switchcase {

	public static void main(String[] args) {
	
		int department = 5;
		switch(department)
		{
			case 1:
				System.out.println("computer Engineering");
			break;
			case 2: 
				System.out.println("E & TC");
			break;
			case 3:
				System.out.println("Information Technology");
			break;
			case 4:
				System.out.println("Civil Engineering");
			break;
			case 5:
				System.out.println("Mechanical Engineering");
			break;
			case 6:
				System.out.println("biomedical Engineering");
			break;
			case 7:
				System.out.println("Chemical Engineering");
			break;
			default : 
				System.out.println("No department found");
		}

	}

}
